    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblPersons.getModel();
        model.setRowCount(0);
        
        for(PersonProfile p : persondirectory.getPersons())
        {
            Object[] row = new Object[7];
            row[0]=p;
            row[1]= p.getLastname();
            row[2]= p.getHomeaddress().getCity();
            row[3]= String.valueOf(p.getHomeaddress().getZipcode());
            row[4]=  p.getWorkaddress().getCity() ;
            row[5]=String.valueOf(p.getWorkaddress().getZipcode());
         
            
            
            model.addRow(row);
        }
    }
