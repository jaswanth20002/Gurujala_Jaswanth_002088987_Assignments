    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedRow = tblPersons.getSelectedRow();
        
        if(selectedRow>=0)
        {
            int dialogueButton = JOptionPane.YES_NO_OPTION;
            int dialougeResult = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete the Person Profile?", "Warning", JOptionPane.WARNING_MESSAGE);
            if(dialougeResult==JOptionPane.YES_OPTION)
            {
                PersonProfile selectedPerson = (PersonProfile) tblPersons.getValueAt(selectedRow,0);
                persondirectory.deleteAccount(selectedPerson);
                populateTable();
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select a person from the list", "Warning", JOptionPane.WARNING_MESSAGE);
        }
            
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        int selectedRow = tblPersons.getSelectedRow();
        
        if(selectedRow>=0)
        {
            PersonProfile selectedPerson = (PersonProfile) tblPersons.getValueAt(selectedRow,0);
            
            ViewPersonJPanel panel = new ViewPersonJPanel(userProcessContainer, persondirectory, selectedPerson);
            userProcessContainer.add("ViewPersonJPanel", panel);
            
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
                   
        }
        else{
            JOptionPane.showMessageDialog(null, "PLease select an account from the list view", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        if(!txtSearch.getText().isBlank())
        {
            String searchstring = txtSearch.getText();
            PersonProfile foundaccount = persondirectory.searchPerson(searchstring);
            
            if(foundaccount!=null)
            {
                ViewPersonJPanel panel = new ViewPersonJPanel(userProcessContainer,persondirectory, foundaccount);
                userProcessContainer.add("ViewPersonJPanel",panel);
                CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                layout.next(userProcessContainer);
            }
            else{
                JOptionPane.showMessageDialog(null, "Person not found", "Error", JOptionPane.ERROR_MESSAGE);
            }
           
                
                
        }
        else{
            JOptionPane.showMessageDialog(null, "please type account no to view", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnSearchActionPerformed


